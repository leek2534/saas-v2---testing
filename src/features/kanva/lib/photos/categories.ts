// Get all images for "All Photos" category
export function getAllPhotos(): string[] {
  return PHOTO_CATEGORIES.filter(cat => cat.id !== 'all')
    .flatMap(cat => cat.images);
}

// Get category by ID
export function getCategoryById(id: string): PhotoCategory | undefined {
  return PHOTO_CATEGORIES.find(cat => cat.id === id);
}

